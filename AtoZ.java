class AtoZ
{
 
    public static void main(String[] args)
    {
        // Declare the variables
        char i='A';
 
        // Display the alphabets
        System.out.printf("The Alphabets from A to Z are: \n");
 
        // Traverse each character
        // with the help of for loop
       while (i <= 'Z') {
			System.out.print(i + " ");
			i++;
		}
    }
}
